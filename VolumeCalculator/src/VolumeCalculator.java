
public class VolumeCalculator {

	// Fill the code
	public double calculateVolume(double radius, double heightCylinder) {
		double volumeOfCylinder = 3.14*radius*radius*heightCylinder;
		return volumeOfCylinder;
	}
	
	public double calculateVolume(int length, int breadth, int height) {
	    double volumeOfCuboid = length*breadth*height;
	    return volumeOfCuboid;
	}
	
}
